import { useState, useEffect } from 'react';
import { supabase } from '~/lib/supabase';
import type { Notification } from '~/types';

interface NotificationBellProps {
  userId?: string;
  userRole?: string;
}

export function NotificationBell({ userId, userRole }: NotificationBellProps) {
  const [notifications, setNotifications] = useState<Notification[]>([]);
  const [unreadCount, setUnreadCount] = useState(0);
  const [isOpen, setIsOpen] = useState(false);
  const [loading, setLoading] = useState(false);

  // ì•Œë¦¼ ì¡°íšŒ í•¨ìˆ˜
  const fetchNotifications = async () => {
    if (!userId) return;
    
    setLoading(true);
    try {
      const { data, error } = await supabase
        .from('notifications')
        .select('*')
        .eq('user_id', userId)
        .order('created_at', { ascending: false })
        .limit(10);

      if (error) {
        console.error('ì•Œë¦¼ ì¡°íšŒ ì˜¤ë¥˜:', error);
        return;
      }

      setNotifications(data || []);
      setUnreadCount(data?.filter(n => n.status === 'unread').length || 0);
    } catch (error) {
      console.error('ì•Œë¦¼ ì¡°íšŒ ì¤‘ ì˜¤ë¥˜:', error);
    } finally {
      setLoading(false);
    }
  };

  // ì•Œë¦¼ ì½ìŒ ì²˜ë¦¬
  const markAsRead = async (notificationId: string) => {
    try {
      const { error } = await supabase
        .from('notifications')
        .update({ status: 'read' })
        .eq('id', notificationId);

      if (!error) {
        setNotifications(prev => 
          prev.map(n => 
            n.id === notificationId ? { ...n, status: 'read' } : n
          )
        );
        setUnreadCount(prev => Math.max(0, prev - 1));
      }
    } catch (error) {
      console.error('ì•Œë¦¼ ì½ìŒ ì²˜ë¦¬ ì˜¤ë¥˜:', error);
    }
  };

  // ëª¨ë“  ì•Œë¦¼ ì½ìŒ ì²˜ë¦¬
  const markAllAsRead = async () => {
    if (!userId || unreadCount === 0) return;

    try {
      const { error } = await supabase
        .from('notifications')
        .update({ status: 'read' })
        .eq('user_id', userId)
        .eq('status', 'unread');

      if (!error) {
        setNotifications(prev => 
          prev.map(n => ({ ...n, status: 'read' }))
        );
        setUnreadCount(0);
      }
    } catch (error) {
      console.error('ëª¨ë“  ì•Œë¦¼ ì½ìŒ ì²˜ë¦¬ ì˜¤ë¥˜:', error);
    }
  };

  // ì‹¤ì‹œê°„ ì•Œë¦¼ êµ¬ë…
  useEffect(() => {
    if (!userId) return;

    fetchNotifications();

    // ì‹¤ì‹œê°„ êµ¬ë… ì„¤ì •
    const subscription = supabase
      .channel('notifications')
      .on(
        'postgres_changes',
        {
          event: 'INSERT',
          schema: 'public',
          table: 'notifications',
          filter: `user_id=eq.${userId}`,
        },
        (payload) => {
          console.log('ìƒˆ ì•Œë¦¼ ìˆ˜ì‹ :', payload);
          const newNotification = payload.new as Notification;
          setNotifications(prev => [newNotification, ...prev.slice(0, 9)]);
          setUnreadCount(prev => prev + 1);
        }
      )
      .on(
        'postgres_changes',
        {
          event: 'UPDATE',
          schema: 'public',
          table: 'notifications',
          filter: `user_id=eq.${userId}`,
        },
        (payload) => {
          console.log('ì•Œë¦¼ ì—…ë°ì´íŠ¸:', payload);
          const updatedNotification = payload.new as Notification;
          setNotifications(prev => 
            prev.map(n => 
              n.id === updatedNotification.id ? updatedNotification : n
            )
          );
        }
      )
      .subscribe();

    return () => {
      subscription.unsubscribe();
    };
  }, [userId]);

  // ì•Œë¦¼ íƒ€ì…ë³„ ì•„ì´ì½˜
  const getNotificationIcon = (type: string) => {
    switch (type) {
      case 'new_order':
        return 'ğŸ›’';
      case 'order_status':
        return 'ğŸ“‹';
      case 'payment_confirmed':
        return 'ğŸ’³';
      default:
        return 'ğŸ“¢';
    }
  };

  // ì‹œê°„ í¬ë§·íŒ…
  const formatTime = (timestamp: string) => {
    const date = new Date(timestamp);
    const now = new Date();
    const diff = now.getTime() - date.getTime();
    const minutes = Math.floor(diff / (1000 * 60));
    const hours = Math.floor(diff / (1000 * 60 * 60));
    const days = Math.floor(diff / (1000 * 60 * 60 * 24));

    if (minutes < 1) return 'ë°©ê¸ˆ ì „';
    if (minutes < 60) return `${minutes}ë¶„ ì „`;
    if (hours < 24) return `${hours}ì‹œê°„ ì „`;
    if (days < 7) return `${days}ì¼ ì „`;
    return date.toLocaleDateString();
  };

  if (!userId) return null;

  return (
    <div className="relative">
      {/* ì•Œë¦¼ ë²¨ ë²„íŠ¼ */}
      <button
        onClick={() => setIsOpen(!isOpen)}
        className="relative p-2 text-gray-600 hover:text-gray-900 focus:outline-none focus:ring-2 focus:ring-wine-500 rounded-lg"
      >
        <svg
          className="w-6 h-6"
          fill="none"
          stroke="currentColor"
          viewBox="0 0 24 24"
        >
          <path
            strokeLinecap="round"
            strokeLinejoin="round"
            strokeWidth={2}
            d="M15 17h5l-1.405-1.405A2.032 2.032 0 0118 14.158V11a6.002 6.002 0 00-4-5.659V5a2 2 0 10-4 0v.341C7.67 6.165 6 8.388 6 11v3.159c0 .538-.214 1.055-.595 1.436L4 17h5m6 0v1a3 3 0 11-6 0v-1m6 0H9"
          />
        </svg>
        
        {/* ì½ì§€ ì•Šì€ ì•Œë¦¼ ì¹´ìš´íŠ¸ */}
        {unreadCount > 0 && (
          <span className="absolute -top-1 -right-1 inline-flex items-center justify-center px-2 py-1 text-xs font-bold leading-none text-white transform translate-x-1/2 -translate-y-1/2 bg-red-600 rounded-full">
            {unreadCount > 99 ? '99+' : unreadCount}
          </span>
        )}
      </button>

      {/* ì•Œë¦¼ ë“œë¡­ë‹¤ìš´ */}
      {isOpen && (
        <div className="absolute right-0 z-50 mt-2 w-80 bg-white rounded-lg shadow-lg border border-gray-200">
          {/* í—¤ë” */}
          <div className="flex items-center justify-between p-4 border-b border-gray-200">
            <h3 className="text-lg font-semibold text-gray-900">ì•Œë¦¼</h3>
            {unreadCount > 0 && (
              <button
                onClick={markAllAsRead}
                className="text-sm text-wine-600 hover:text-wine-800"
              >
                ëª¨ë‘ ì½ìŒ
              </button>
            )}
          </div>

          {/* ì•Œë¦¼ ëª©ë¡ */}
          <div className="max-h-96 overflow-y-auto">
            {loading ? (
              <div className="p-4 text-center text-gray-500">
                ë¡œë”© ì¤‘...
              </div>
            ) : notifications.length === 0 ? (
              <div className="p-4 text-center text-gray-500">
                ìƒˆë¡œìš´ ì•Œë¦¼ì´ ì—†ìŠµë‹ˆë‹¤
              </div>
            ) : (
              notifications.map((notification) => (
                <div
                  key={notification.id}
                  className={`p-4 border-b border-gray-100 cursor-pointer hover:bg-gray-50 ${
                    notification.status === 'unread' ? 'bg-blue-50' : ''
                  }`}
                  onClick={() => markAsRead(notification.id)}
                >
                  <div className="flex items-start space-x-3">
                    <span className="text-lg flex-shrink-0 mt-1">
                      {getNotificationIcon(notification.type)}
                    </span>
                    <div className="flex-1 min-w-0">
                      <p className={`text-sm ${
                        notification.status === 'unread' 
                          ? 'text-gray-900 font-medium' 
                          : 'text-gray-600'
                      }`}>
                        {notification.message}
                      </p>
                      <p className="text-xs text-gray-500 mt-1">
                        {formatTime(notification.created_at)}
                      </p>
                    </div>
                    {notification.status === 'unread' && (
                      <div className="w-2 h-2 bg-blue-600 rounded-full flex-shrink-0 mt-2" />
                    )}
                  </div>
                </div>
              ))
            )}
          </div>

          {/* í‘¸í„° */}
          {notifications.length > 0 && (
            <div className="p-3 bg-gray-50 text-center border-t border-gray-200">
              <button className="text-sm text-wine-600 hover:text-wine-800">
                ëª¨ë“  ì•Œë¦¼ ë³´ê¸°
              </button>
            </div>
          )}
        </div>
      )}

      {/* ë“œë¡­ë‹¤ìš´ ì™¸ë¶€ í´ë¦­ ì‹œ ë‹«ê¸° */}
      {isOpen && (
        <div
          className="fixed inset-0 z-40"
          onClick={() => setIsOpen(false)}
        />
      )}
    </div>
  );
}